<script>
export default {
  name: "Logout",
  data() {
    return {
      count: ''
    }
  },
  created() {
    this.redirectToLogin()
  },
  methods: {
    redirectToLogin() {
      const TIME_COUNT = 3
      if (!this.timer) {
        this.count = TIME_COUNT
        this.show = false
        this.timer = setInterval(() => {
          if (this.count > 0 && this.count <= TIME_COUNT) {
            this.count--
          } else {
            this.show = true
            clearInterval(this.timer)
            this.timer = null
            this.$router.push('/login')
          }
        }, 1000)
      }
    }
  }
